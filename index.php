        // Hero Slider Functionality
        let currentSlide = 0;
        const slides = document.querySelectorAll('.slide');
        let heroSliderInterval = setInterval(nextSlide, 5000);
        let isHeroHovered = false;

        function nextSlide() {
            if (!isHeroHovered) {
                currentSlide = (currentSlide + 1) % slides.length;
                updateSlider();
            }
        }

        function updateSlider() {
            const slider = document.getElementById('heroSlider');
            slider.style.transform = `translateX(-${currentSlide * 100}%)`;
        }

        slides.forEach(slide => {
            slide.addEventListener('mouseenter', () => {
                isHeroHovered = true;
                clearInterval(heroSliderInterval);
            });
            slide.addEventListener('mouseleave', () => {
                isHeroHovered = false;
                heroSliderInterval = setInterval(nextSlide, 5000);
            });
        });

        // Room Slider Functionality
        let currentRoomSlide = 0;
        const roomSlides = document.querySelectorAll('.room-slide');
        let roomSliderInterval = setInterval(nextRoomSlide, 5000);
