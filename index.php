        });

        // About Section Slider
        let aboutSlideIndex = 1;
        const totalAboutSlides = 3;
        let aboutSliderInterval;

        function showAboutSlide(n) {
            if (n > totalAboutSlides) aboutSlideIndex = 1;
            if (n < 1) aboutSlideIndex = totalAboutSlides;

            const slides = document.getElementById('aboutSlides');
            slides.style.transform = `translateX(-${(aboutSlideIndex - 1) * 100}%)`;

            const dots = document.querySelectorAll('.nav-dot');
            dots.forEach((dot, index) => {
                dot.classList.toggle('active', index === aboutSlideIndex - 1);
            });
        }

        function changeAboutSlide(n) {
            aboutSlideIndex = n;
            showAboutSlide(aboutSlideIndex);
        }

        function nextAboutSlide() {
            aboutSlideIndex++;
            showAboutSlide(aboutSlideIndex);
        }

        function startAboutSlider() {
            aboutSliderInterval = setInterval(nextAboutSlide, 4000);
        }

        startAboutSlider();

        document.querySelectorAll('.nav-dot').forEach((dot, index) => {
